#include<stdio.h>
//局部变量不初始化是野指针，全局变量不初始化默认0
int i;//全局变量
int main()
{
	
	i--;
	if (i > sizeof(i))
		//sizeof(i)计算变量/类型所占空间大小（>=0)
		//当整型和无符号数比较时，先将整型转化为无符号数
		//-1原码10000000 00000000 00000000 00000001
		//反码11111111 11111111 11111111 11111110
		//补码11111111 11111111 11111111 11111111
		//z转变为无符号数后1不再是符号位。i原反补都是32个1
		printf(">\n");
	else if (i < sizeof(i))
		printf("<");
	else
		printf("=\n");

}