#include<stdio.h>

//整型提升
int main()
{
	char a = 3;
	//将3放入a中，00000000 00000000 00000000 00000011.
	//只存放1个字节在a中：00000011
	char b = 127;
	//00000000 00000000 00000000 01111111.
	//在b中存放01111111
	char c = a + b;
	//char a,b;是有符号数，按照符号位提升补0再相加
	//00000000 00000000 00000000 00000011+00000000 00000000 00000000 01111111
	//=00000000 00000000 00000000 10000010再将10000010存放在c中
	//10000010的反码10000001原码11111110=-126
	printf("%d", c);//返回c=-126
	//
}